//리스트는 데이터를 순서대로 나열한 구조
//노드는 리스트에 있는 개별 요소들
//head node-제일 처음에 있는 노드, tail node-제일 마지막에 있는 노드
//predecessor node-하나의 노드를 기준으로 바로 앞에 노드
//successor node-하나의 노드를 기준으로 바로 뒤에 노드

class List1 {
	
	int arr[];
	int size;
	
	public void createList(int number) {
		//배열의 크기를 10인 배열 생성
		arr = new int[number];
		size = 0;
	}

	public void delete(List1 a, int x) {
		//해당값을 찾아서 삭제하기
		for (int i = 0; i < size; i++) {
			if (a.arr[i] == x) {
				while (i < size - 1) {
					a.arr[i] = a.arr[i + 1];
					size--;
				}
			}
		}
	}

	public void printall(List1 a) {
		//전체 출력하기
		System.out.println();
		for (int i = 0; i < size; i++) {
			System.out.printf("%3d", a.arr[i]);
		}
	}

	public void insert(List1 a, int i, int x) {
		//리스트에 값 삽입하기
		int len = size;
		while (i != len) {
			
		}
		a.arr[len] = x;
		size++;
	}

	public boolean isEmpty(List1 a) {
		//배열에 비어있는지 확인하기
		return a.size == 0;
	}
	
}